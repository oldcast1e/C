/*한자리 양의 정수를 두 개 입력받아, 입력받은 정수를 해당 수만큼 각각 한 줄에 출력하라.
Ex)
## Input ##
3 5
## Output ##
333
55555
*/
#include  <stdio.h>
int main() 
{
    int n,m;
    scanf("%d %d",&n,&m);
    for(int i=0;i<n;i++){
        printf("%d",n);
    }
    printf("\n");
    for(int k=0;k<m;k++){
        printf("%d",m);
    }
    

    

}